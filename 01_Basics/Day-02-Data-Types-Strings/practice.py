# Day 2 - Data Types & Strings

# Data types
age = 20
height = 5.6
name = "Usha"
is_student = True

print(type(age))
print(type(height))
print(type(name))
print(type(is_student))

# String operations
first_name = "Usha"
last_name = "M"

full_name = first_name + " " + last_name
print("Full Name:", full_name)

# Repetition
print("Hello! " * 3)

# String functions
text = "  python is fun  "

print(text.upper())
print(text.lower())
print(text.title())
print(text.strip())

# Length
print("Length:", len(text))

# Indexing & slicing
word = "Python"

print(word[0])
print(word[-1])
print(word[0:3])
print(word[2:])
